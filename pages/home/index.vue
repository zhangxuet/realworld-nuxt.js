<template>
  <div class="home-page">

    <div class="banner">
        <div class="container">
        <h1 class="logo-font">conduit</h1>
        <p>A place to share your knowledge.</p>
        </div>
    </div>

    <div class="container page">
        <div class="row">

        <div class="col-md-9">
            <div class="feed-toggle">
            <ul class="nav nav-pills outline-active">
                <li class="nav-item" v-if="user">
                    <nuxt-link
                        class="nav-link"
                        :class="{ active: tab === 'Your_Feed' }"
                        exact
                        :to="{
                            name: 'home',
                            query: {
                                tab: 'Your_Feed'
                            }
                        }">
                        Your Feed
                    </nuxt-link>
                </li>
                <li class="nav-item">
                    <nuxt-link
                        class="nav-link"
                        :class="{ active: tab === 'Global_Feed' }"
                        exact
                        :to="{
                            name: 'home',
                            query: {
                                tab: 'Global_Feed'
                            }
                        }">
                        Global Feed
                    </nuxt-link>
                </li>
                <li class="nav-item" v-if="tag">
                    <nuxt-link
                        class="nav-link"
                        :class="{ active: tab === 'tag' }"
                        exact
                        :to="{
                            name: 'home',
                            query: {
                                tab: 'tag',
                                tag: tag
                            }
                        }">
                        #{{ tag }}
                    </nuxt-link>
                </li>
            </ul>
            </div>

            <div class="article-preview" v-for="article in articles" :key="article.slug">
                <div class="article-meta">
                    <nuxt-link
                        :to="{
                            name: 'profile',
                            params: {
                                username: article.author.username
                            }
                        }"
                    >
                        <img :src="article.author.image" />
                    </nuxt-link>
                    <div class="info">
                    <nuxt-link
                        class="author"
                        :to="{
                            name: 'profile',
                            params: {
                                username: article.author.username
                            }
                        }"
                    >
                    {{ article.author.username }}
                    </nuxt-link>
                    <span class="date">{{ article.createdAt | date('MMM DD, YYYY') }}</span>
                    </div>
                    <button
                        class="btn btn-outline-primary btn-sm pull-xs-right"
                        :class="{active: article.favorited}"
                        @click="onFavorite(article)"
                        :disabled="article.favoriteDisabled"
                    >
                        <i class="ion-heart"></i> {{ article.favoritesCount }}
                    </button>
                </div>
                <nuxt-link
                    :to="{
                        name: 'article',
                        params: {
                            slug: article.slug
                        }
                    }"
                    class="preview-link"
                >
                    <h1>{{ article.title }}</h1>
                    <p>{{ article.description }}</p>
                    <span>Read more...</span>
                </nuxt-link>
            </div>

            <!-- 分页 -->
            <nav>
                <ul class="pagination">
                    <li class="page-item" :class="{active: item === page}" v-for="item in totalPage" :key="item">
                        <nuxt-link
                            class="page-link ng-binding"
                            :to="{
                                name: 'home',
                                query: {
                                    page: item,
                                    tag: tag,
                                    tab: tab
                                }
                            }"
                        >
                        {{ item }}
                        </nuxt-link>
                    </li>
                </ul>
            </nav>

        </div>

        <div class="col-md-3">
            <div class="sidebar">
            <p>Popular Tags</p>

            <div class="tag-list">
                <nuxt-link
                    :to="{
                        name: 'home',
                        query: {
                            tag: item,
                            tab: 'tag'
                        }
                    }"
                    class="tag-pill tag-default"
                    v-for="item in tags" :key="item"
                >
                    {{ item }}
                </nuxt-link>
            </div>
            </div>
        </div>

        </div>
    </div>

    </div>
</template>

<script>
import { mapState } from 'vuex'
import {
    getArticles,
    getFeedArticles,
    addFavorite,
    deleteFavorite
} from '@/api/article'
import { getTags } from '@/api/tags'

export default {
    name: 'HomeIndex',
    watchQuery: ['page', 'tag', 'tab'],
    async asyncData({ query, store }) {
        const page = Number.parseInt(query.page || 1)
        const limit = 10
        const { tag } = query
        const tab = query.tab || 'Global_Feed'
        const loadArticle = store.state.user && tab === 'Your_Feed' ? getFeedArticles : getArticles
        const [ articleRes, tagRes ] = await Promise.all([
            loadArticle({
                limit,
                offset: (page - 1) * limit,
                tag
            }),
            getTags()
        ])
        const { articles, articlesCount } = articleRes.data
        const { tags } = tagRes.data

        articles.forEach(item => item.favoriteDisabled = false)

        return {
            articles,
            articlesCount,
            limit,
            page,
            tags,
            tag,
            tab
        }
    },
    computed: {
        ...mapState(['user']),
        totalPage () {
            return Math.ceil(this.articlesCount / this.limit)
        }
    },
    data() {
        return {
        
        }
    },
    methods: {
        async onFavorite(article) {
            article.favoriteDisabled = true
            if (article.favorited) {
                const res = await deleteFavorite(article.slug)
                article.favorited = false
                article.favoritesCount -= 1
            } else {
                const res = await addFavorite(article.slug)
                article.favorited = true
                article.favoritesCount += 1
            }
            article.favoriteDisabled = false
        },
    },
}
</script>